let value = NaN;
value &&= 10; // = 10 сработыет если NaN истинно. NaN ложно, поэтому value остается NaN
value ||= 20; // = 20 сработыет если NaN ложно, NaN ложно, поэтому value становится 20
value &&= 30; // = 30 сработает если value истинно, value теперь 20, 20 истинно, так что value теперь 30
value ||= 40; // = 40 сработает если value ложно, value = 30, 30 истинно, поэтому value остается 30
alert(value); // 30

